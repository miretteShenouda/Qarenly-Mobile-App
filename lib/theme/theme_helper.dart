/// Class containing the supported color schemes.
class ColorSchemes {
  static final primaryColorScheme = ColorScheme.light(
    // Primary colors
    primary: Color(0XFCFFFFFF),
    primaryContainer: Color(0XFF1877F2),

    // Error colors
    errorContainer: Color(0XFFEE9648),

    // On colors(text colors)
    onPrimary: Color(0XFF03314C),
    onPrimaryContainer: Color(0XFF003049),
    onSecondaryContainer: Color(0XFFDEEDF4),
  );
}

/// Class containing custom colors for a primary theme.
class PrimaryColors {
  // Amber
  Color get amber500 => Color(0XFFFFC107);

  // Black
  Color get black900 => Color(0XFF000000);

  // BlueGray
  Color get blueGray300 => Color(0XFF94A8B3);

  // Orange
  Color get orange400 => Color(0XFFFF9F1C);

  // TealEc
  Color get teal100Ec => Color(0XECA9CADA);
}

PrimaryColors get appTheme => ThemeHelper().themeColor();
ThemeData get theme => ThemeHelper().themeData();
